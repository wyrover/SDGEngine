	TileSet::TileSet(const std::string &str, float scalex, float scaley, int layer)
		: width(0), height(0), textureOffset(0.f, 0.f), textureScale(scalex, scaley), position(0.f, 0.f), cellid(nullptr), layerNum(layer)
	{
		texture = MySingleton<Assets>::GetSingleton()->RequestTexture(str);
		if (texture)
		{
			width = texture->width();
			height = texture->height();
		}

		cellid = new unsigned int[layerNum];
		assert(width && height);
		windex = unsigned(width / textureScale.x);
		hindex = unsigned(height / textureScale.y);
		xwidth = sizeof(layer1[0]) / sizeof(short);
		yheight = (sizeof(layer1) / sizeof(short)) / xwidth;
	}

	TileSet::~TileSet()
	{
		SDELETE(texture);
		SDELETE(cellid);
	}

	void TileSet::Render()
	{
		Graphics::SetAlphatest(true);
		Graphics::BindTexture(texture);
		for (unsigned x = 0; x < xwidth; x++)
		{
			position.x = x * textureScale.x;
			for (unsigned y = 0; y < yheight; y++)
			{
				position.y = y * textureScale.y;
				for (unsigned l = 0; l < layerNum; l++)
				{
					switch (l)
					{
					case 0:
						cellid[l] = layer1[y][x];
						break;
					case 1:
						cellid[l] = layer2[y][x];
						break;
					default:
						std::cout << "no layer detected!" << std::endl;
						break;
					}
					textureOffset.x = (cellid[l] % windex) * textureScale.x;
					textureOffset.y = (cellid[l] / hindex) * textureScale.y;
					Graphics::RenderQuad(width, height, textureOffset, textureScale, position);
				}
			}
		}
		Graphics::SetAlphatest(false);
		Graphics::BindTexture(nullptr);
	}
